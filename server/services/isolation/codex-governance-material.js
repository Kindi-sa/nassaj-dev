/**
 * codex-governance-material — the neutral Codex governance file, materialized as a
 * read-only per-user COPY (not a symlink) with content-identity verification.
 *
 * SECURITY (2026-07-12 remediation, supersedes the symlink mechanism of ADR-057
 * for the governance FILE specifically):
 *
 *   A Codex turn runs danger-full-access (routes/agent.js bypassPermissions ->
 *   sandboxMode 'danger-full-access'). If $CODEX_HOME/AGENTS.md were a SYMLINK to
 *   the shared fleet-wide neutral source (~/.claude/AGENTS.md -> nassaj-core), a
 *   turn could write THROUGH the link and corrupt governance for EVERY user on the
 *   node. So governance is materialized as a real, read-only (0444) per-user COPY:
 *   the worst a hostile/compromised turn can do is damage its OWN copy — which the
 *   next spawn's fail-closed guard detects by content fingerprint and rewrites — and
 *   it can NEVER reach the shared source. (A same-uid full-access agent can chmod its
 *   own copy and overwrite it; that is caught before the next turn and only ever
 *   affects that one user's next turn, never the fleet source.)
 *
 * IDENTITY, not mere existence: "governed" means the copy is a real, non-empty file
 * whose sha256 EQUALS the neutral source's — not just "a file exists" (a subverted
 * or stale file would pass a size>0 check). Drift (source regenerated by build-agents)
 * or corruption both fail the identity check and trigger a rewrite.
 *
 * No internal imports: this module is the shared primitive used by BOTH the
 * provisioning fast path (provision-user-dirs.js) and the fail-closed spawn guard
 * (codex-governance.ts), so it must not import either (no cycle).
 */

import crypto from 'node:crypto';
import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';

/** The governance filename Codex/opencode ingest from $CODEX_HOME. */
export const CODEX_AGENTS_FILENAME = 'AGENTS.md';

/**
 * Read-only mode for a materialized governance copy. A same-uid agent can still
 * chmod+rewrite it, but 0444 stops accidental/naive writes and signals intent; the
 * hard guarantee is the per-turn fingerprint re-check + the copy (not link) itself.
 */
const GOVERNANCE_FILE_MODE = 0o444;

/**
 * The neutral governance source: ~/.claude/AGENTS.md — the same operator-home base
 * the Claude CLAUDE.md/NASSAJ.md provisioning links use. Resolves (via the
 * ~/.claude -> nassaj-core bootstrap symlink) to nassaj-core/AGENTS.md, the
 * build-agents NEUTRAL output. Using the operator-home base (not a hardcoded
 * nassaj-core path) keeps this portable across fleet nodes.
 *
 * @returns {string}
 */
export function neutralGovernanceSource() {
  return path.join(os.homedir(), '.claude', CODEX_AGENTS_FILENAME);
}

/** sha256 hex of a buffer. */
function fingerprintOf(content) {
  return crypto.createHash('sha256').update(content).digest('hex');
}

/**
 * Reads the neutral governance source content (following the ~/.claude ->
 * nassaj-core symlink chain — READS are safe, only writes-through are the risk).
 *
 * @returns {{ content: Buffer, fingerprint: string } | null} null when the source
 *   is absent or empty (cannot govern).
 */
export function readNeutralGovernance() {
  try {
    const content = fs.readFileSync(neutralGovernanceSource());
    if (content.length === 0) {
      return null;
    }
    return { content, fingerprint: fingerprintOf(content) };
  } catch {
    return null;
  }
}

/**
 * True iff the governance file at `agentsPath` is a real, non-empty regular file
 * (NOT a symlink) whose content fingerprint MATCHES the current neutral source —
 * i.e. authentic, current governance. A symlink, a missing/empty file, or any drift
 * (corrupted, stale, or subverted content) returns false so the fail-closed guard
 * repairs or blocks. When the source itself is unreadable this returns false
 * (governance cannot be attested).
 *
 * lstat (not stat) is deliberate: a symlink must NOT be accepted as governed —
 * that is the write-through corruption vector this module exists to close.
 *
 * @param {string} agentsPath
 * @returns {boolean}
 */
export function governanceMatchesSource(agentsPath) {
  const src = readNeutralGovernance();
  if (!src) {
    return false;
  }
  try {
    const st = fs.lstatSync(agentsPath);
    // lstat on a symlink reports isFile()===false, so this rejects links too.
    if (!st.isFile()) {
      return false;
    }
    const current = fs.readFileSync(agentsPath);
    if (current.length === 0) {
      return false;
    }
    return fingerprintOf(current) === src.fingerprint;
  } catch {
    return false;
  }
}

/**
 * Materializes governance as a real, read-only (0444) COPY at
 * <codexHome>/AGENTS.md, (re)writing it whenever it is missing, a symlink, empty,
 * or drifted from the neutral source. A COPY — never a symlink — is the security
 * invariant (see module header). Never throws.
 *
 * @param {string} codexHome the effective CODEX_HOME whose AGENTS.md to establish
 * @returns {boolean} whether governance matches the source after the attempt
 */
export function materializeGovernanceCopy(codexHome) {
  const agentsPath = path.join(codexHome, CODEX_AGENTS_FILENAME);
  if (governanceMatchesSource(agentsPath)) {
    return true;
  }
  const src = readNeutralGovernance();
  if (!src) {
    // No neutral source on this node (build-agents output absent) — cannot govern;
    // the caller blocks the launch (fail-closed).
    return false;
  }
  try {
    fs.mkdirSync(codexHome, { recursive: true });
    // Remove whatever is there first: a stale copy, a dangling/hostile symlink, or a
    // wrong-type entry. rmSync removes the directory ENTRY (needs write on codexHome,
    // owned 0700 by us) — it does NOT follow a symlink to write the target, so the
    // shared source is never touched.
    fs.rmSync(agentsPath, { force: true });
    // Write 0600 first (umask-safe, never a transient world-readable window) then
    // tighten to 0444.
    fs.writeFileSync(agentsPath, src.content, { mode: 0o600 });
    fs.chmodSync(agentsPath, GOVERNANCE_FILE_MODE);
  } catch (err) {
    console.error('[Codex] governance copy materialize FAILED — Codex sessions BLOCKED', {
      codexHome,
      error: err instanceof Error ? err.message : String(err),
    });
    return false;
  }
  return governanceMatchesSource(agentsPath);
}
